namespace Akeldov.Math.Spatial2D.Curves
{
    /// <summary>
    /// Specifies the angular traversal direction around a circle.
    /// </summary>
    public enum AngularDirection
    {
        /// <summary>
        /// Traverses angles in the counterclockwise direction.
        /// </summary>
        Counterclockwise = 1,

        /// <summary>
        /// Traverses angles in the clockwise direction.
        /// </summary>
        Clockwise = -1
    }
}
